package com.flavio.generalcomparator;

class Teste{
    private String s;
    private Integer i;
    private Long l;

    public Teste(String s, Integer i, Long l) {
        this.s = s;
        this.i = i;
        this.l = l;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public Integer getI() {
        return i;
    }

    public void setI(Integer i) {
        this.i = i;
    }

    public Long getL() {
        return l;
    }

    public void setL(Long l) {
        this.l = l;
    }
}